class BancoDoStarkMarvel(BancoDoStark):
    def validar_heroi_marvel(self, hero_id: int, session=requests):
        dados = consultar_heroi_por_id(hero_id, session=session)
        if not dados:
            print("ERRO - Herói não encontrado na API.")
            return None
        publisher = dados.get("biography", {}).get("publisher")
        if publisher != "Marvel Comics":
            print("ERRO - O herói encontrado não pertence à Marvel Comics.")
            return None
        print(f"OK - Herói validado: {dados['name']} ({publisher})")
        return dados

    def criar_conta_com_heroi(self, titular: str, hero_id: int, session=requests):
        heroi = self.validar_heroi_marvel(hero_id, session=session)
        if not heroi:
            print("Conta não criada por falha na validação externa.")
            return False
        conta_criada = self.criar_conta(titular)
        if conta_criada:
            self.contas[titular].extrato.append(("validacao_api", hero_id, heroi["name"]))
            print(f"OK - Conta vinculada ao herói {heroi['name']}!")
            return True
        return False

