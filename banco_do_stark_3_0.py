BASE_API = "https://cdn.jsdelivr.net/gh/akabab/superhero-api@0.3.0/api"

def consultar_heroi_por_id(hero_id: int, session=requests):
    url = f"{BASE_API}/id/{hero_id}.json"
    print("Consultando URL:", url)
    try:
        resposta = session.get(url, timeout=20)
        print("Status code:", resposta.status_code)
        if resposta.status_code != 200:
            print("ERRO - Não foi possível encontrar o herói na API.")
            return None
        return resposta.json()
    except Exception as erro:
        print("ERRO de comunicação com a API:", erro)
        return None

